function [Helm] = HelmholtzEOS(obj, T, rho)
%% Input
Tc = obj.Tc;
rhoc = obj.rhoc;
R = obj.R;

tau = Tc / T;
delta = rho / rhoc;

a1 = obj.a1;
a2 = obj.a2;
c0 = obj.c0;
c1 = obj.c1;
c2 = obj.c2;

v = obj.v;
u = obj.u;

fluid_state = obj.fluid_state;

n = obj.n;

%% System
if c1~=0 && c2~=0
	term_c = - (c1 * Tc^c2) / (c2*(c2+1)) .* tau.^(-c2);
else
	term_c = zeros(size(tau));
end
phio = a1 ...
	+ a2.*tau ...
	+ log(delta) ...
	+ (c0-1).*log(tau) ...
    + term_c  ...
    + sum( v .* log( 1 - exp(-u .* tau./Tc) ), 2 );

switch fluid_state
    case 0          % Non‑Polar
        phir = ...
              n(1)  *  delta        * tau^(0.25)  ...                        
            + n(2)  *  delta        * tau^(1.125) ...                        
            + n(3)  *  delta        * tau^(1.5)   ...                        
            + n(4)  * (delta^2)     * tau^(1.375) ...                        
            + n(5)  * (delta^3)     * tau^(0.25)  ...                        
            + n(6)  * (delta^7)     * tau^(0.875) ...                        
            + n(7)  * (delta^2)     * tau^(0.625) * exp(-delta)   ...        
            + n(8)  * (delta^5)     * tau^(1.75)  * exp(-delta)   ...        
            + n(9)  *  delta        * tau^(3.625) * exp(-delta^2) ...        
            + n(10) * (delta^4)     * tau^(3.625) * exp(-delta^2) ...        
            + n(11) * (delta^3)     * tau^(14.5)  * exp(-delta^3) ...        
            + n(12) * (delta^4)     * tau^(12.0)  * exp(-delta^3);
    case 1          % Polar
        phir = ...
              n(1)  *  delta        * tau^(0.25) ...
            + n(2)  *  delta        * tau^(1.25) ...
            + n(3)  *  delta        * tau^(1.5)  ...
            + n(4)  * (delta^3)     * tau^(0.25) ...
            + n(5)  * (delta^7)     * tau^(0.875) ...
            + n(6)  *  delta        * tau^(2.375) * exp(-delta) ...
            + n(7)  * (delta^2)     * tau^(2.0)   * exp(-delta) ...
            + n(8)  * (delta^5)     * tau^(2.125) * exp(-delta) ...
            + n(9)  *  delta        * tau^(3.5)   * exp(-delta^2) ...
            + n(10) *  delta        * tau^(6.5)   * exp(-delta^2) ...
            + n(11) * (delta^4)     * tau^(4.75)  * exp(-delta^2) ...
            + n(12) * (delta^2)     * tau^(12.5)  * exp(-delta^3);
    otherwise
        error('fluid_state must be 0 (Non‑Polar) or 1 (Polar).');
end

phi = phio + phir;

phio_delta = 1 / delta;
phio_delta2 = - 1 / delta^2;
phio_delta_tau = 0;

x = exp(-u .* tau ./ Tc);
einstein_tau_terms = v .* (u./Tc) .* x ./ (1 - x);
phio_tau = ...
      a2 ...
    + (c0 - 1) ./ tau ...
    + (c1 * Tc^c2) / (c2 + 1) .* tau.^(-(c2 + 1)) ...
    + sum(einstein_tau_terms);
einstein_tau2_terms = v .* (u.^2./Tc^2) .* x ./ (1 - x).^2;
phio_tau2 = ...
   -(c0 - 1) ./ tau.^2 ...
   -  c1 * Tc^c2        .* tau.^(-(c2 + 2)) ...
   -  sum(einstein_tau2_terms);

switch fluid_state
    case 0          % Non‑Polar
		phir_delta = ...
	      n(1)  * tau^(0.25) ...
	    + n(2)  * tau^(1.125) ...
	    + n(3)  * tau^(1.5)   ...
	    + 2*n(4)  * delta      * tau^(1.375) ...
	    + 3*n(5)  * delta^2    * tau^(0.25)  ...
	    + 7*n(6)  * delta^6    * tau^(0.875) ...
	    + n(7)  * tau^(0.625)  * exp(-delta)   * (2*delta - delta^2) ...
	    + n(8)  * tau^(1.75)   * exp(-delta)   * (5*delta^4 - delta^5) ...
	    + n(9)  * tau^(3.625)  * exp(-delta^2) * (1 - 2*delta^2) ...
	    + n(10) * tau^(3.625)  * exp(-delta^2) * (4*delta^3 - 2*delta^5) ...
	    + n(11) * tau^(14.5)   * exp(-delta^3) * (3*delta^2 - 3*delta^5) ...
	    + n(12) * tau^(12.0)   * exp(-delta^3) * (4*delta^3 - 3*delta^6);
	      
		phir_delta2 = ...
	      2*n(4)  * tau^(1.375) ...
	    + 6*n(5)  * delta      * tau^(0.25)  ...
	    + 42*n(6) * delta^5    * tau^(0.875) ...
	    + n(7)  * tau^(0.625)  * exp(-delta)   * (delta^2      - 4*delta      + 2) ...
	    + n(8)  * tau^(1.75)   * exp(-delta)   * (delta^5      -10*delta^4    + 20*delta^3) ...
	    + n(9)  * tau^(3.625)  * exp(-delta^2) * (4*delta^3    - 6*delta) ...
	    + n(10) * tau^(3.625)  * exp(-delta^2) * (4*delta^6    -18*delta^4    + 12*delta^2) ...
	    + n(11) * tau^(14.5)   * exp(-delta^3) * (9*delta^7    -24*delta^4    + 6*delta) ...
	    + n(12) * tau^(12.0)   * exp(-delta^3) * (9*delta^8    -30*delta^5    + 12*delta^2);
	      
		phir_delta_tau = ...
	      0.25  * n(1)  * tau^(-0.75) ...
	    + 1.125 * n(2)  * tau^(0.125) ...
	    + 1.5   * n(3)  * tau^(0.5)   ...
	    + 2.75  * n(4)  * delta      * tau^(0.375) ...
	    + 0.75  * n(5)  * delta^2    * tau^(-0.75) ...
	    + 6.125 * n(6)  * delta^6    * tau^(-0.125) ...
	    + 0.625 * n(7)  * tau^(-0.375) * exp(-delta)   * (2*delta - delta^2) ...
	    + 1.75  * n(8)  * tau^(0.75)  * exp(-delta)   * (5*delta^4 - delta^5) ...
	    + 3.625 * n(9)  * tau^(2.625) * exp(-delta^2) * (1 - 2*delta^2) ...
	    + 3.625 * n(10) * tau^(2.625) * exp(-delta^2) * (4*delta^3 - 2*delta^5) ...
	    + 14.5  * n(11) * tau^(13.5)  * exp(-delta^3) * (3*delta^2 - 3*delta^5) ...
	    + 12.0  * n(12) * tau^(11.0)  * exp(-delta^3) * (4*delta^3 - 3*delta^6);
	      
	    phir_tau = ...
	      0.25  * n(1)  * delta      * tau^(-0.75) ...
	    + 1.125 * n(2)  * delta      * tau^(0.125) ...
	    + 1.5   * n(3)  * delta      * tau^(0.5)   ...
	    + 1.375 * n(4)  * delta^2    * tau^(0.375) ...
	    + 0.25  * n(5)  * delta^3    * tau^(-0.75) ...
	    + 0.875 * n(6)  * delta^7    * tau^(-0.125) ...
	    + 0.625 * n(7)  * delta^2    * tau^(-0.375) * exp(-delta) ...
	    + 1.75  * n(8)  * delta^5    * tau^(0.75)   * exp(-delta) ...
	    + 3.625 * n(9)  * delta      * tau^(2.625)  * exp(-delta^2) ...
	    + 3.625 * n(10) * delta^4    * tau^(2.625)  * exp(-delta^2) ...
	    + 14.5  * n(11) * delta^3    * tau^(13.5)   * exp(-delta^3) ...
	    + 12.0  * n(12) * delta^4    * tau^(11.0)   * exp(-delta^3);
	      
		phir_tau2 = ...
		-0.1875    * n(1)  * delta      * tau^(-1.75) ...
		+ 0.140625 * n(2)  * delta      * tau^(-0.875) ...
		+ 0.75     * n(3)  * delta      * tau^(-0.5)   ...
		+ 0.515625 * n(4)  * delta^2    * tau^(-0.625) ...
		- 0.1875   * n(5)  * delta^3    * tau^(-1.75)  ...
		- 0.109375 * n(6)  * delta^7    * tau^(-1.125) ...
		- 0.234375 * n(7)  * delta^2    * tau^(-1.375) .* exp(-delta) ...
		+ 1.3125   * n(8)  * delta^5    * tau^(-0.25)  .* exp(-delta) ...
		+ 9.515625 * n(9)  * delta      * tau^(1.625)  .* exp(-delta.^2) ...
		+ 9.515625 * n(10) * delta^4    * tau^(1.625)  .* exp(-delta.^2) ...
		+ 195.75   * n(11) * delta^3    * tau^(12.5)   .* exp(-delta.^3) ...
		+ 132.0    * n(12) * delta^4    * tau^(10.0)   .* exp(-delta.^3);
		  
    case 1          % Polar
        phir_delta = ...
	      n(1)  * tau^(0.25) ...
	    + n(2)  * tau^(1.25) ...
	    + n(3)  * tau^(1.5) ...
	    + 3*n(4)  * delta^2   * tau^(0.25) ...
	    + 7*n(5)  * delta^6   * tau^(0.875) ...
	    + n(6)  * tau^(2.375) * exp(-delta)   * (1        - delta) ...
	    + n(7)  * tau^(2.0)   * exp(-delta)   * (2*delta  - delta^2) ...
	    + n(8)  * tau^(2.125) * exp(-delta)   * (5*delta^4 - delta^5) ...
	    + n(9)  * tau^(3.5)   * exp(-delta^2) * (1        - 2*delta^2) ...
	    + n(10) * tau^(6.5)   * exp(-delta^2) * (1        - 2*delta^2) ...
	    + n(11) * tau^(4.75)  * exp(-delta^2) * (4*delta^3 - 2*delta^5) ...
	    + n(12) * tau^(12.5)  * exp(-delta^3) * (2*delta   - 3*delta^4);
        
	    phir_delta2 = ...
	      6  * n(4)  * delta      * tau^(0.25) ...
	    + 42 * n(5)  * delta^5    * tau^(0.875) ...
	    + n(6)  * tau^(2.375) * exp(-delta)   .* ( delta         - 2 ) ...
	    + n(7)  * tau^(2.0)   * exp(-delta)   .* ( delta.^2      - 4*delta + 2 ) ...
	    + n(8)  * tau^(2.125) * exp(-delta)   .* ( delta.^5      -10*delta.^4 + 20*delta.^3 ) ...
	    + n(9)  * tau^(3.5)   * exp(-delta.^2).* ( 4*delta.^3    - 6*delta ) ...
	    + n(10) * tau^(6.5)   * exp(-delta.^2).* ( 4*delta.^3    - 6*delta ) ...
	    + n(11) * tau^(4.75)  * exp(-delta.^2).* ( 4*delta.^6    -18*delta.^4 + 12*delta.^2 ) ...
	    + n(12) * tau^(12.5)  * exp(-delta.^3).* ( 9*delta.^6    -18*delta.^3 + 2 );
	      
		phir_delta_tau = ...
	      0.25  * n(1)  * tau^(-0.75) ...
	    + 1.25  * n(2)  * tau^(0.25)  ...
	    + 1.5   * n(3)  * tau^(0.5)   ...
	    + 0.75  * n(4)  * delta^2   * tau^(-0.75) ...
	    + 6.125 * n(5)  * delta^6   * tau^(-0.125) ...
	    + 2.375 * n(6)  * tau^(1.375) * exp(-delta)   .* (1        - delta) ...
	    + 2.0   * n(7)  * tau^(1.0)   * exp(-delta)   .* (2*delta  - delta.^2) ...
	    + 2.125 * n(8)  * tau^(1.125) * exp(-delta)   .* (5*delta.^4 - delta.^5) ...
	    + 3.5   * n(9)  * tau^(2.5)   * exp(-delta.^2).* (1        - 2*delta.^2) ...
	    + 6.5   * n(10) * tau^(5.5)   * exp(-delta.^2).* (1        - 2*delta.^2) ...
	    + 4.75  * n(11) * tau^(3.75)  * exp(-delta.^2).* (4*delta.^3 - 2*delta.^5) ...
	    + 12.5  * n(12) * tau^(11.5)  * exp(-delta.^3).* (2*delta   - 3*delta.^4);
	      
		phir_tau = ...
	      0.25  * n(1)  * delta      * tau^(-0.75) ...
	    + 1.25  * n(2)  * delta      * tau^(0.25)  ...
	    + 1.5   * n(3)  * delta      * tau^(0.5)   ...
	    + 0.25  * n(4)  * delta^3    * tau^(-0.75) ...
	    + 0.875 * n(5)  * delta^7    * tau^(-0.125) ...
	    + 2.375 * n(6)  * delta      * tau^(1.375) .* exp(-delta) ...
	    + 2.0   * n(7)  * delta^2    * tau^(1.0)   .* exp(-delta) ...
	    + 2.125 * n(8)  * delta^5    * tau^(1.125) .* exp(-delta) ...
	    + 3.5   * n(9)  * delta      * tau^(2.5)   .* exp(-delta.^2) ...
	    + 6.5   * n(10) * delta      * tau^(5.5)   .* exp(-delta.^2) ...
	    + 4.75  * n(11) * delta^4    * tau^(3.75)  .* exp(-delta.^2) ...
	    + 12.5  * n(12) * delta^2    * tau^(11.5)  .* exp(-delta.^3);
	      
		phir_tau2 = ...
		-0.1875    * n(1)  * delta      * tau^(-1.75) ...
		+ 0.3125    * n(2)  * delta      * tau^(-0.75) ...
		+ 0.75      * n(3)  * delta      * tau^(-0.5)  ...
		- 0.1875    * n(4)  * delta^3    * tau^(-1.75) ...
		- 0.109375  * n(5)  * delta^7    * tau^(-1.125) ...
		+ 3.265625  * n(6)  * delta      * tau^(0.375)  .* exp(-delta) ...
		+ 2.0       * n(7)  * delta^2    .* exp(-delta) ...
		+ 2.390625  * n(8)  * delta^5    * tau^(0.125)  .* exp(-delta) ...
		+ 8.75      * n(9)  * delta      * tau^(1.5)    .* exp(-delta.^2) ...
		+ 35.75     * n(10) * delta      * tau^(4.5)    .* exp(-delta.^2) ...
		+ 17.8125   * n(11) * delta^4    * tau^(2.75)   .* exp(-delta.^2) ...
		+ 143.75    * n(12) * delta^2    * tau^(10.5)   .* exp(-delta.^3);
        
    otherwise
        error('fluid_state must be 0 (Non‑Polar) or 1 (Polar).');
end

Z = 1 + delta * phir_delta;
P = Z * rho * R * T;
u = (R * T) * tau * (phio_tau + phir_tau);
h = (R * T) * (1 + tau * (phio_tau + phir_tau) + delta * phir_delta);
s = R * (tau * (phio_tau + phir_tau) - phio - phir);
cv = R * (- tau^2 * (phio_tau2 + phir_tau2));
cp = R * (-tau^2*(phio_tau2 + phir_tau2) + ((1 + delta*phir_delta - delta*tau*phir_delta_tau)^2) / (1 + 2*delta*phir_delta + delta^2*phir_delta2));
c = sqrt((R * T) * (1 + 2*delta*phir_delta + delta^2*phir_delta2 - ((1 + delta*phir_delta - delta*tau*phir_delta_tau)^2) / (tau^2*(phio_tau2 + phir_tau2))));
g = R * T * ( 1 + phio + phir + delta * phir_delta);

%% Output
Helm.Z = Z;
Helm.P = P; % Pa
Helm.T = T; % K

Helm.u = u; % J/kg
Helm.h = h; % J/kg
Helm.s = s; % J/kg*K

Helm.cv = cv; % J/kg*K
Helm.cp = cp; % J/kg*K

Helm.c = c; % m/s
Helm.g = g; % J/kg
end