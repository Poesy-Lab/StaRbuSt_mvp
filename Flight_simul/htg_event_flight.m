% % % RK4의 궤적 분석 시 지면을 뚫고 내려가는 것을 방지합니다.
function stop_event = htg_event_flight(t, x)
    % 이 함수는 시뮬레이션을 중단해야 할 경우 true를 반환합니다.
    % x(3)은 지상 좌표계 Z축 위치 (아래 방향이 양수)이므로, -x(3)이 고도입니다.
    altitude = -x(3,1); 

    % 시뮬레이션 초기에는 (예: t < 0.1초) 지상에 있더라도 중단하지 않습니다.
    % 이는 로켓이 실제로 이륙할 시간을 주기 위함입니다.
    % 이 시간(0.1초)은 dt (현재 0.01초) 및 로켓의 초기 가속도에 따라 조정될 수 있습니다.
    if t < 0.2 % 수정: 이륙을 위한 시간 0.2초로 연장
        stop_event = false;
    else
        % 설정된 시간 이후에 고도가 0 이하가 되면 중단 (지면 충돌)
        stop_event = (altitude <= 0);
    end
end